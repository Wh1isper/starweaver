fn collect_refs(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    renderer_input_variants: &BTreeMap<String, Vec<String>>,
    output: &mut BTreeSet<String>,
    visiting: &mut BTreeSet<String>,
) -> Result<(), String> {
    match kind {
        SchemaKind::Ref(name) => {
            if !output.insert(name.clone()) {
                return Ok(());
            }
            if !visiting.insert(name.clone()) {
                return Err(format!("recursive renderer Rust schema {name}"));
            }
            let schema = &ir.schemas[name].kind;
            if let Some(allowed) = renderer_input_variants.get(name) {
                let SchemaKind::OneOf { variants, .. } = schema else {
                    return Err(format!(
                        "renderer input variant target {name} is not a union"
                    ));
                };
                for variant in variants.iter().filter(|variant| {
                    matches!(variant, SchemaKind::Ref(target) if allowed.contains(target))
                }) {
                    collect_refs(
                        ir,
                        variant,
                        renderer_input_variants,
                        output,
                        visiting,
                    )?;
                }
            } else {
                collect_refs(ir, schema, renderer_input_variants, output, visiting)?;
            }
            visiting.remove(name);
        }
        SchemaKind::Object(object) => {
            for kind in object.properties.values() {
                collect_refs(ir, kind, renderer_input_variants, output, visiting)?;
            }
        }
        SchemaKind::Array { items, .. } => {
            collect_refs(ir, items, renderer_input_variants, output, visiting)?;
        }
        SchemaKind::OneOf { variants, .. } => {
            for variant in variants {
                collect_refs(ir, variant, renderer_input_variants, output, visiting)?;
            }
        }
        SchemaKind::JsonObject
        | SchemaKind::String(_)
        | SchemaKind::Integer { .. }
        | SchemaKind::Boolean
        | SchemaKind::Null => {}
    }
    Ok(())
}

fn render_rust_named_schema(
    ir: &ProtocolIr,
    name: &str,
    renderer_input_variants: &BTreeMap<String, Vec<String>>,
) -> Result<String, String> {
    let kind = &ir.schemas[name].kind;
    Ok(match kind {
        SchemaKind::String(value) if !value.enum_values.is_empty() => {
            let mut out =
                String::from("#[derive(Clone, Debug, Eq, PartialEq, Deserialize, Serialize)]\n");
            out.push_str(&format!("pub enum {} {{\n", rust_type_name(name)));
            for item in &value.enum_values {
                out.push_str(&format!(
                    "    #[serde(rename = {})]\n    {},\n",
                    json_string(item),
                    pascal(item)
                ));
            }
            out.push_str("}\n");
            out
        }
        SchemaKind::String(value) if value.const_value.is_some() => {
            let item = value.const_value.as_deref().unwrap_or_default();
            format!(
                "#[derive(Clone, Debug, Eq, PartialEq, Deserialize, Serialize)]\npub enum {} {{ #[serde(rename = {})] Value }}\n",
                rust_type_name(name),
                json_string(item)
            )
        }
        SchemaKind::String(_) => format!(
            "#[derive(Clone, Debug, Eq, PartialEq, Deserialize, Serialize)]\n#[serde(transparent)]\npub struct {}(pub String);\n",
            rust_type_name(name)
        ),
        SchemaKind::Integer { .. } => format!("pub type {} = i64;\n", rust_type_name(name)),
        SchemaKind::Boolean => format!("pub type {} = bool;\n", rust_type_name(name)),
        SchemaKind::Null => format!("pub type {} = ();\n", rust_type_name(name)),
        SchemaKind::Array { items, .. } => format!(
            "pub type {} = Vec<{}>;\n",
            rust_type_name(name),
            rust_kind_type(items)?
        ),
        SchemaKind::Object(object) => render_rust_object(name, object)?,
        SchemaKind::JsonObject => {
            return Err(format!(
                "arbitrary JSON schema {name} cannot cross the Desktop renderer boundary"
            ));
        }
        SchemaKind::OneOf {
            variants,
            discriminator,
            ..
        } => {
            let projected_variants = renderer_input_variants.get(name).map_or_else(
                || variants.clone(),
                |allowed| {
                    variants
                        .iter()
                        .filter(|variant| {
                            matches!(variant, SchemaKind::Ref(target) if allowed.contains(target))
                        })
                        .cloned()
                        .collect::<Vec<_>>()
                },
            );
            render_rust_union(ir, name, &projected_variants, discriminator.as_deref())?
        }
        SchemaKind::Ref(target) => format!(
            "pub type {} = {};\n",
            rust_type_name(name),
            rust_type_name(target)
        ),
    })
}

fn render_rust_object(name: &str, object: &ObjectSchema) -> Result<String, String> {
    let mut out = String::from(
        "#[derive(Clone, Debug, Deserialize, Serialize)]\n#[serde(deny_unknown_fields, rename_all = \"camelCase\")]\n",
    );
    out.push_str(&format!("pub struct {} {{\n", rust_type_name(name)));
    for (field, kind) in &object.properties {
        out.push_str(&format!(
            "    pub {}: {},\n",
            rust_field(field),
            rust_field_type(kind, object.required.contains(field))?
        ));
    }
    out.push_str("}\n");
    Ok(out)
}

fn render_rust_union(
    ir: &ProtocolIr,
    name: &str,
    variants: &[SchemaKind],
    discriminator: Option<&str>,
) -> Result<String, String> {
    let non_null = variants
        .iter()
        .filter(|variant| !matches!(variant, SchemaKind::Null))
        .collect::<Vec<_>>();
    if non_null.len() == 1 && non_null.len() != variants.len() {
        return Ok(format!(
            "pub type {} = Option<{}>;\n",
            rust_type_name(name),
            rust_kind_type(non_null[0])?
        ));
    }
    let mut out = String::from("#[derive(Clone, Debug, Deserialize, Serialize)]\n");
    if let Some(tag) = discriminator {
        out.push_str(&format!("#[serde(tag = {})]\n", json_string(tag)));
    } else {
        out.push_str("#[serde(untagged)]\n");
    }
    out.push_str(&format!("pub enum {} {{\n", rust_type_name(name)));
    for (index, variant) in non_null.iter().enumerate() {
        if let (Some(tag), SchemaKind::Ref(target)) = (discriminator, *variant) {
            let object = object_schema(ir, target)?;
            let tag_value = object
                .properties
                .get(tag)
                .and_then(|kind| match kind {
                    SchemaKind::String(value) => value.const_value.as_deref(),
                    _ => None,
                })
                .ok_or_else(|| format!("union {name} variant {target} has no const tag {tag}"))?;
            out.push_str(&format!(
                "    #[serde(rename = {})]\n    {} {{\n",
                json_string(tag_value),
                pascal(tag_value)
            ));
            for (field, kind) in &object.properties {
                if field == tag {
                    continue;
                }
                out.push_str(&format!(
                    "        {}: {},\n",
                    rust_field(field),
                    rust_field_type(kind, object.required.contains(field))?
                ));
            }
            out.push_str("    },\n");
        } else {
            out.push_str(&format!(
                "    V{}({}),\n",
                index + 1,
                rust_kind_type(variant)?
            ));
        }
    }
    out.push_str("}\n");
    Ok(out)
}

fn rust_field_type(kind: &SchemaKind, required: bool) -> Result<String, String> {
    let (base, nullable) = match kind {
        SchemaKind::OneOf { variants, .. }
            if variants
                .iter()
                .any(|variant| matches!(variant, SchemaKind::Null)) =>
        {
            let non_null = variants
                .iter()
                .filter(|variant| !matches!(variant, SchemaKind::Null))
                .collect::<Vec<_>>();
            if non_null.len() != 1 {
                return Err(
                    "nullable renderer union must contain exactly one non-null variant".to_string(),
                );
            }
            (rust_kind_type(non_null[0])?, true)
        }
        _ => (rust_kind_type(kind)?, false),
    };
    if !required || nullable {
        Ok(format!("Option<{base}>"))
    } else {
        Ok(base)
    }
}

fn rust_kind_type(kind: &SchemaKind) -> Result<String, String> {
    Ok(match kind {
        SchemaKind::Ref(name) => rust_type_name(name),
        SchemaKind::String(_) => "String".to_string(),
        SchemaKind::Integer { .. } => "i64".to_string(),
        SchemaKind::Boolean => "bool".to_string(),
        SchemaKind::Null => "()".to_string(),
        SchemaKind::JsonObject => {
            return Err("arbitrary JSON cannot cross the Desktop renderer boundary".to_string());
        }
        SchemaKind::Array { items, .. } => format!("Vec<{}>", rust_kind_type(items)?),
        SchemaKind::OneOf { variants, .. } => {
            let non_null = variants
                .iter()
                .filter(|variant| !matches!(variant, SchemaKind::Null))
                .collect::<Vec<_>>();
            if non_null.len() != 1 {
                return Err("anonymous Rust union requires one non-null variant".to_string());
            }
            format!("Option<{}>", rust_kind_type(non_null[0])?)
        }
        SchemaKind::Object(_) => {
            return Err("anonymous object cannot cross the Desktop renderer boundary".to_string());
        }
    })
}

fn rustfmt_source(source: &str) -> Result<String, String> {
    let mut child = Command::new("rustfmt")
        .args(["--edition", "2024", "--emit", "stdout"])
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .map_err(|error| format!("start rustfmt for generated Desktop bindings: {error}"))?;
    child
        .stdin
        .take()
        .ok_or("rustfmt stdin unavailable")?
        .write_all(source.as_bytes())
        .map_err(|error| error.to_string())?;
    let output = child
        .wait_with_output()
        .map_err(|error| error.to_string())?;
    if !output.status.success() {
        return Err(format!(
            "generated Desktop Rust failed rustfmt: {}",
            String::from_utf8_lossy(&output.stderr)
        ));
    }
    String::from_utf8(output.stdout).map_err(|error| error.to_string())
}

