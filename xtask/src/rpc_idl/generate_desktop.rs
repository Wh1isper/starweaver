fn safe_ts_type(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    depth: usize,
    visiting: &mut BTreeSet<String>,
) -> Result<String, String> {
    if depth > MAX_INLINE_SCHEMA_DEPTH {
        return Err("Desktop safe projection expansion exceeded depth limit".to_string());
    }
    Ok(match kind {
        SchemaKind::Ref(name) => {
            if !visiting.insert(name.clone()) {
                return Err(format!("recursive Desktop safe projection schema {name}"));
            }
            let value = if name == "HostEvent" {
                let SchemaKind::OneOf { variants, .. } = &ir.schemas[name].kind else {
                    return Err("HostEvent must remain a generated closed union".to_string());
                };
                variants
                    .iter()
                    .filter(|variant| is_desktop_event_variant(ir, variant))
                    .map(|variant| safe_ts_type(ir, variant, depth + 1, visiting))
                    .collect::<Result<Vec<_>, _>>()?
                    .join(" | ")
            } else {
                safe_ts_type(ir, &ir.schemas[name].kind, depth + 1, visiting)?
            };
            visiting.remove(name);
            value
        }
        SchemaKind::String(StringSchema {
            enum_values,
            const_value,
            ..
        }) if !enum_values.is_empty() => enum_values
            .iter()
            .map(|value| json_string(value))
            .collect::<Vec<_>>()
            .join(" | "),
        SchemaKind::String(StringSchema {
            const_value: Some(value),
            ..
        }) => json_string(value),
        SchemaKind::String(_) => "string".to_string(),
        SchemaKind::Integer { .. } => "number".to_string(),
        SchemaKind::Boolean => "boolean".to_string(),
        SchemaKind::Null => "null".to_string(),
        SchemaKind::JsonObject => {
            return Err("arbitrary JSON cannot enter a Desktop safe projection".to_string());
        }
        SchemaKind::Array { items, .. } => format!(
            "readonly ({})[]",
            safe_ts_type(ir, items, depth + 1, visiting)?
        ),
        SchemaKind::Object(object) => {
            let mut value = String::from("{ ");
            for (field, field_kind) in &object.properties {
                if is_forbidden_projected_field(field) {
                    continue;
                }
                let optional = if object.required.contains(field) {
                    ""
                } else {
                    "?"
                };
                value.push_str(&format!(
                    "readonly {}{}: {}; ",
                    ts_property(field),
                    optional,
                    safe_ts_type(ir, field_kind, depth + 1, visiting)?
                ));
            }
            value.push('}');
            value
        }
        SchemaKind::OneOf { variants, .. } => variants
            .iter()
            .map(|variant| safe_ts_type(ir, variant, depth + 1, visiting))
            .collect::<Result<Vec<_>, _>>()?
            .join(" | "),
    })
}

fn is_desktop_event_variant(ir: &ProtocolIr, variant: &SchemaKind) -> bool {
    let SchemaKind::Ref(schema_name) = variant else {
        return false;
    };
    DESKTOP_EVENT_KINDS.iter().any(|event_kind| {
        ir.event_classes
            .get(*event_kind)
            .is_some_and(|class| class.schema_type == *schema_name)
    })
}

fn is_forbidden_projected_field(field: &str) -> bool {
    matches!(
        field,
        "acceptedCursor"
            | "cursor"
            | "deliverySequence"
            | "diagnosticRef"
            | "fenceCursor"
            | "fingerprint"
            | "idempotencyKey"
            | "nextCursor"
            | "nextDeliverySequence"
            | "scope"
            | "subscriptionId"
    )
}

