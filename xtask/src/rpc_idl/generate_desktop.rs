fn insert(files: &mut BTreeMap<PathBuf, Vec<u8>>, path: &str, source: String) {
    let mut normalized = source.replace("\r\n", "\n").replace('\r', "\n");
    if !normalized.ends_with('\n') {
        normalized.push('\n');
    }
    files.insert(PathBuf::from(path), normalized.into_bytes());
}

fn is_paginated(surface: &SurfaceOperation) -> bool {
    surface
        .input
        .supervisor_overridden
        .iter()
        .any(|field| field == "cursor")
        && surface.output.transform.iter().any(|field| field == "page")
}

fn pascal(value: &str) -> String {
    let mut output = String::new();
    let mut uppercase = true;
    for character in value.chars() {
        if character.is_ascii_alphanumeric() {
            if uppercase {
                output.push(character.to_ascii_uppercase());
                uppercase = false;
            } else {
                output.push(character);
            }
        } else {
            uppercase = true;
        }
    }
    output
}

fn camel(value: &str) -> String {
    let pascal = pascal(value);
    let mut characters = pascal.chars();
    characters
        .next()
        .map(|first| first.to_ascii_lowercase().to_string() + characters.as_str())
        .unwrap_or_default()
}

fn rust_type_name(value: &str) -> String {
    pascal(value)
}

fn rust_field(value: &str) -> String {
    let mut output = String::new();
    for (index, character) in value.chars().enumerate() {
        if character.is_ascii_uppercase() && index > 0 {
            output.push('_');
        }
        if character.is_ascii_alphanumeric() {
            output.push(character.to_ascii_lowercase());
        } else if !output.ends_with('_') {
            output.push('_');
        }
    }
    match output.as_str() {
        "type" | "match" | "ref" | "self" | "crate" | "mod" | "move" => {
            format!("r#{output}")
        }
        _ => output,
    }
}

fn ts_property(value: &str) -> String {
    if is_ts_identifier(value) {
        value.to_string()
    } else {
        json_string(value)
    }
}

fn ts_access(base: &str, field: &str) -> String {
    if is_ts_identifier(field) {
        format!("{base}.{field}")
    } else {
        format!("{base}[{}]", json_string(field))
    }
}

fn is_ts_identifier(value: &str) -> bool {
    value.chars().enumerate().all(|(index, character)| {
        character == '_'
            || character == '$'
            || (character.is_ascii_alphanumeric() && (index > 0 || !character.is_ascii_digit()))
    })
}

fn json_string(value: &str) -> String {
    serde_json::to_string(value).unwrap_or_else(|_| "\"\"".to_string())
}
