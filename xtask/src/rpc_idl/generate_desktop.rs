#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields, rename_all = "camelCase")]
struct SurfaceManifest {
    #[serde(rename = "$schema")]
    schema: String,
    schema_version: u32,
    protocol: SurfaceProtocol,
    commands: SurfaceCommands,
    renderer_input_variants: BTreeMap<String, Vec<String>>,
    operations: BTreeMap<String, SurfaceOperation>,
    notifications: BTreeMap<String, SurfaceNotification>,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct SurfaceProtocol {
    name: String,
    major: u32,
    digest: String,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields, rename_all = "camelCase")]
struct SurfaceCommands {
    acknowledge: String,
    acknowledge_operation: String,
    execute: String,
    list_pending_operations: String,
    subscribe: String,
    unsubscribe: String,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct SurfaceOperation {
    input: InputAuthority,
    output: OutputAuthority,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields, rename_all = "camelCase")]
struct InputAuthority {
    renderer_provided: Vec<String>,
    supervisor_constructed: Vec<String>,
    supervisor_overridden: Vec<String>,
    forbidden: Vec<String>,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct OutputAuthority {
    select: Vec<String>,
    transform: Vec<String>,
    forbidden: Vec<String>,
}

#[derive(Clone, Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct SurfaceNotification {
    profile: String,
    projection: String,
    output: OutputAuthority,
}

pub fn generate(repository: &Path, ir: &ProtocolIr) -> Result<BTreeMap<PathBuf, Vec<u8>>, String> {
    let manifest = load_manifest(repository)?;
    validate_manifest(ir, &manifest)?;

    let mut outputs = BTreeMap::new();
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src/generated/host/types.ts",
        render_typescript_types(ir, &manifest)?,
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src/generated/host/validators.ts",
        render_typescript_validators(ir, &manifest),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src/generated/host/client.ts",
        render_typescript_client(&manifest),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src/generated/host/index.ts",
        render_typescript_index(),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/src/generated/host/mod.rs",
        rustfmt_source(&render_rust(ir, &manifest)?)?,
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/acknowledge_host_event.toml",
        render_permission(
            ACKNOWLEDGE_COMMAND,
            "Acknowledge one supervisor-issued Desktop host event delivery",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/acknowledge_host_operation.toml",
        render_permission(
            ACKNOWLEDGE_OPERATION_COMMAND,
            "Acknowledge one supervisor-issued Desktop host operation result",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/list_pending_host_operations.toml",
        render_permission(
            LIST_PENDING_OPERATIONS_COMMAND,
            "List unresolved renderer-safe Desktop host operation handles",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/execute_host_operation.toml",
        render_permission(
            EXECUTE_COMMAND,
            "Execute one generated renderer-safe host operation",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/subscribe_host_events.toml",
        render_permission(
            SUBSCRIBE_COMMAND,
            "Subscribe to generated renderer-safe host events",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/permissions/autogenerated/unsubscribe_host_events.toml",
        render_permission(
            UNSUBSCRIBE_COMMAND,
            "Unsubscribe a supervisor-issued host event token",
        ),
    );
    insert(
        &mut outputs,
        "apps/starweaver-desktop/src-tauri/capabilities/generated-host.json",
        render_capability_fragment(ir, &manifest)?,
    );
    Ok(outputs)
}

#[allow(dead_code)]
pub fn validate_manifest_value(ir: &ProtocolIr, value: Value) -> Result<(), String> {
    let manifest: SurfaceManifest = serde_json::from_value(value)
        .map_err(|error| format!("invalid Desktop host-surface manifest: {error}"))?;
    validate_manifest(ir, &manifest)
}

fn load_manifest(repository: &Path) -> Result<SurfaceManifest, String> {
    let path = repository.join(MANIFEST_PATH);
    let value = super::source::load_yaml(&path)?;
    let schema_path = path
        .parent()
        .ok_or_else(|| format!("{} has no parent", path.display()))?
        .join("manifest.schema.json");
    let schema: Value = serde_json::from_slice(
        &fs::read(&schema_path)
            .map_err(|error| format!("read {}: {error}", schema_path.display()))?,
    )
    .map_err(|error| format!("parse {}: {error}", schema_path.display()))?;
    let validator = jsonschema::options()
        .with_draft(jsonschema::Draft::Draft7)
        .build(&schema)
        .map_err(|error| format!("compile {}: {error}", schema_path.display()))?;
    if let Err(error) = validator.validate(&value) {
        return Err(format!(
            "{} rejected Desktop host surface: {error}",
            schema_path.display()
        ));
    }
    serde_json::from_value(value)
        .map_err(|error| format!("parse {} as typed manifest: {error}", path.display()))
}

fn validate_manifest(ir: &ProtocolIr, manifest: &SurfaceManifest) -> Result<(), String> {
    if manifest.schema != MANIFEST_SCHEMA || manifest.schema_version != 1 {
        return Err("Desktop host surface must use the reviewed schema version 1".to_string());
    }
    if manifest.protocol.name != ir.identity.name || manifest.protocol.major != ir.identity.major {
        return Err("Desktop host surface protocol identity does not match ProtocolIr".to_string());
    }
    if manifest.protocol.digest != ir.identity.schema_digest {
        return Err(format!(
            "Desktop host surface digest must match the reviewed protocol digest {}",
            ir.identity.schema_digest
        ));
    }
    if manifest.commands.acknowledge != ACKNOWLEDGE_COMMAND
        || manifest.commands.acknowledge_operation != ACKNOWLEDGE_OPERATION_COMMAND
        || manifest.commands.execute != EXECUTE_COMMAND
        || manifest.commands.list_pending_operations != LIST_PENDING_OPERATIONS_COMMAND
        || manifest.commands.subscribe != SUBSCRIBE_COMMAND
        || manifest.commands.unsubscribe != UNSUBSCRIBE_COMMAND
    {
        return Err(
            "Desktop host surface command names are not the fixed reviewed commands".to_string(),
        );
    }

    validate_renderer_input_variants(ir, manifest)?;

    let expected = REQUIRED_DESKTOP_METHODS
        .iter()
        .map(|value| (*value).to_string())
        .collect::<BTreeSet<_>>();
    let actual = manifest.operations.keys().cloned().collect::<BTreeSet<_>>();
    if actual != expected {
        return Err(format!(
            "Desktop operation inventory mismatch: missing {:?}, unexpected {:?}",
            expected.difference(&actual).collect::<Vec<_>>(),
            actual.difference(&expected).collect::<Vec<_>>()
        ));
    }

    for (name, surface) in &manifest.operations {
        let method = ir
            .methods
            .get(name)
            .ok_or_else(|| format!("Desktop operation {name} is not a public IDL method"))?;
        let params = object_schema(ir, &method.params_type)?;
        validate_input_authority(
            ir,
            name,
            params,
            &surface.input,
            &manifest.renderer_input_variants,
        )?;
        let result = object_schema(ir, &method.result_type)?;
        validate_output_authority(ir, name, result, &surface.output)?;
    }

    if manifest.notifications.len() != 1 || !manifest.notifications.contains_key("host.event") {
        return Err(
            "Desktop surface must expose exactly the canonical host.event notification".to_string(),
        );
    }
    for (name, surface) in &manifest.notifications {
        let notification = ir
            .notifications
            .get(name)
            .ok_or_else(|| format!("Desktop notification {name} is not public IDL"))?;
        let params = object_schema(ir, &notification.params_type)?;
        validate_output_authority(ir, name, params, &surface.output)?;
        let profile = ir.event_profiles.get(&surface.profile).ok_or_else(|| {
            format!(
                "Desktop notification {name} has unknown profile {}",
                surface.profile
            )
        })?;
        let allowed = DESKTOP_EVENT_KINDS.iter().copied().collect::<BTreeSet<_>>();
        for event_kind in profile {
            if !allowed.contains(event_kind.as_str()) {
                return Err(format!(
                    "Desktop profile {} contains unauthorized event kind {event_kind}",
                    surface.profile
                ));
            }
        }
        if surface.projection.is_empty()
            || !surface
                .projection
                .starts_with(|character: char| character.is_ascii_uppercase())
        {
            return Err(format!(
                "Desktop notification {name} has invalid projection name"
            ));
        }
    }
    Ok(())
}

fn validate_renderer_input_variants(
    ir: &ProtocolIr,
    manifest: &SurfaceManifest,
) -> Result<(), String> {
    for (schema_name, allowed_variants) in &manifest.renderer_input_variants {
        let schema = ir
            .schemas
            .get(schema_name)
            .ok_or_else(|| format!("unknown renderer input union {schema_name}"))?;
        let SchemaKind::OneOf { variants, .. } = &schema.kind else {
            return Err(format!(
                "renderer input variant policy target {schema_name} is not a union"
            ));
        };
        let available = variants
            .iter()
            .filter_map(|variant| match variant {
                SchemaKind::Ref(name) => Some(name.as_str()),
                _ => None,
            })
            .collect::<BTreeSet<_>>();
        if available.len() != variants.len() {
            return Err(format!(
                "renderer input union {schema_name} must contain only named variants"
            ));
        }
        let allowed = allowed_variants
            .iter()
            .map(String::as_str)
            .collect::<BTreeSet<_>>();
        if allowed.is_empty()
            || allowed.len() != allowed_variants.len()
            || !allowed.is_subset(&available)
        {
            return Err(format!(
                "renderer input union {schema_name} has an invalid variant allowlist"
            ));
        }
    }
    Ok(())
}

fn validate_input_authority(
    ir: &ProtocolIr,
    operation: &str,
    schema: &ObjectSchema,
    authority: &InputAuthority,
    renderer_input_variants: &BTreeMap<String, Vec<String>>,
) -> Result<(), String> {
    let groups = [
        ("rendererProvided", &authority.renderer_provided),
        ("supervisorConstructed", &authority.supervisor_constructed),
        ("supervisorOverridden", &authority.supervisor_overridden),
        ("forbidden", &authority.forbidden),
    ];
    validate_partition(operation, schema.properties.keys(), &groups)?;
    for field in &authority.renderer_provided {
        if is_supervisor_owned_name(field) {
            return Err(format!(
                "Desktop renderer cannot own supervisor field {operation}.{field}"
            ));
        }
        let kind = schema
            .properties
            .get(field)
            .ok_or_else(|| format!("missing field {operation}.{field}"))?;
        ensure_renderer_input_safe(
            ir,
            kind,
            renderer_input_variants,
            &format!("{operation}.{field}"),
            0,
            &mut BTreeSet::new(),
        )?;
    }
    for field in &authority.forbidden {
        if schema.required.contains(field) {
            return Err(format!(
                "required field {operation}.{field} cannot be forbidden because complete params could not be built"
            ));
        }
    }
    Ok(())
}

fn validate_output_authority(
    ir: &ProtocolIr,
    symbol: &str,
    schema: &ObjectSchema,
    authority: &OutputAuthority,
) -> Result<(), String> {
    let groups = [
        ("select", &authority.select),
        ("transform", &authority.transform),
        ("forbidden", &authority.forbidden),
    ];
    validate_partition(symbol, schema.properties.keys(), &groups)?;
    for field in &authority.select {
        let kind = schema
            .properties
            .get(field)
            .ok_or_else(|| format!("missing output field {symbol}.{field}"))?;
        ensure_direct_output_safe(
            ir,
            kind,
            &format!("{symbol}.{field}"),
            0,
            &mut BTreeSet::new(),
        )?;
    }
    for field in &authority.transform {
        let kind = schema
            .properties
            .get(field)
            .ok_or_else(|| format!("missing transformed output field {symbol}.{field}"))?;
        ensure_transformed_output_safe(
            ir,
            kind,
            &format!("{symbol}.{field}"),
            0,
            &mut BTreeSet::new(),
        )?;
    }
    Ok(())
}

fn validate_partition<'a>(
    symbol: &str,
    fields: impl Iterator<Item = &'a String>,
    groups: &[(&str, &Vec<String>)],
) -> Result<(), String> {
    let expected = fields.cloned().collect::<BTreeSet<_>>();
    let mut classified = BTreeMap::<String, String>::new();
    for (group, values) in groups {
        let mut local = BTreeSet::new();
        for field in *values {
            if !local.insert(field) {
                return Err(format!("{symbol}.{field} is duplicated in {group}"));
            }
            if let Some(previous) = classified.insert(field.clone(), (*group).to_string()) {
                return Err(format!(
                    "{symbol}.{field} belongs to both {previous} and {group}"
                ));
            }
        }
    }
    let actual = classified.keys().cloned().collect::<BTreeSet<_>>();
    if actual != expected {
        return Err(format!(
            "field authority for {symbol} is incomplete: missing {:?}, unknown {:?}",
            expected.difference(&actual).collect::<Vec<_>>(),
            actual.difference(&expected).collect::<Vec<_>>()
        ));
    }
    Ok(())
}

fn ensure_renderer_input_safe(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    renderer_input_variants: &BTreeMap<String, Vec<String>>,
    path: &str,
    depth: usize,
    visiting: &mut BTreeSet<String>,
) -> Result<(), String> {
    if depth > MAX_INLINE_SCHEMA_DEPTH {
        return Err(format!("renderer schema exceeds depth limit at {path}"));
    }
    match kind {
        SchemaKind::Ref(name) => {
            if is_forbidden_schema_name(name) {
                return Err(format!(
                    "renderer field {path} references forbidden schema {name}"
                ));
            }
            if !visiting.insert(name.clone()) {
                return Err(format!(
                    "renderer field {path} contains recursive schema {name}"
                ));
            }
            let schema = ir
                .schemas
                .get(name)
                .ok_or_else(|| format!("unknown schema {name} at {path}"))?;
            if let Some(allowed) = renderer_input_variants.get(name) {
                let SchemaKind::OneOf { variants, .. } = &schema.kind else {
                    return Err(format!(
                        "renderer input variant target {name} is not a union"
                    ));
                };
                for variant in variants.iter().filter(|variant| {
                    matches!(variant, SchemaKind::Ref(target) if allowed.contains(target))
                }) {
                    ensure_renderer_input_safe(
                        ir,
                        variant,
                        renderer_input_variants,
                        path,
                        depth + 1,
                        visiting,
                    )?;
                }
            } else {
                ensure_renderer_input_safe(
                    ir,
                    &schema.kind,
                    renderer_input_variants,
                    path,
                    depth + 1,
                    visiting,
                )?;
            }
            visiting.remove(name);
        }
        SchemaKind::JsonObject => {
            return Err(format!(
                "renderer field {path} cannot contain an arbitrary JSON object"
            ));
        }
        SchemaKind::Object(object) => {
            for (field, value) in &object.properties {
                if is_sensitive_name(field) {
                    return Err(format!("renderer field {path}.{field} is sensitive"));
                }
                ensure_renderer_input_safe(
                    ir,
                    value,
                    renderer_input_variants,
                    &format!("{path}.{field}"),
                    depth + 1,
                    visiting,
                )?;
            }
        }
        SchemaKind::Array { items, .. } => {
            ensure_renderer_input_safe(
                ir,
                items,
                renderer_input_variants,
                path,
                depth + 1,
                visiting,
            )?;
        }
        SchemaKind::OneOf { variants, .. } => {
            for variant in variants {
                ensure_renderer_input_safe(
                    ir,
                    variant,
                    renderer_input_variants,
                    path,
                    depth + 1,
                    visiting,
                )?;
            }
        }
        SchemaKind::String(_)
        | SchemaKind::Integer { .. }
        | SchemaKind::Boolean
        | SchemaKind::Null => {}
    }
    Ok(())
}

fn ensure_direct_output_safe(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    path: &str,
    depth: usize,
    visiting: &mut BTreeSet<String>,
) -> Result<(), String> {
    if depth > MAX_INLINE_SCHEMA_DEPTH {
        return Err(format!("selected output exceeds depth limit at {path}"));
    }
    match kind {
        SchemaKind::Ref(name) => {
            if is_forbidden_schema_name(name) {
                return Err(format!(
                    "selected output {path} references forbidden schema {name}"
                ));
            }
            if !visiting.insert(name.clone()) {
                return Err(format!(
                    "selected output {path} contains recursive schema {name}"
                ));
            }
            let schema = ir
                .schemas
                .get(name)
                .ok_or_else(|| format!("unknown schema {name} at {path}"))?;
            ensure_direct_output_safe(ir, &schema.kind, path, depth + 1, visiting)?;
            visiting.remove(name);
        }
        SchemaKind::JsonObject => {
            return Err(format!(
                "selected output {path} cannot expose an arbitrary JSON object"
            ));
        }
        SchemaKind::Object(object) => {
            for (field, value) in &object.properties {
                if is_sensitive_name(field) {
                    return Err(format!("selected output {path}.{field} is sensitive"));
                }
                ensure_direct_output_safe(
                    ir,
                    value,
                    &format!("{path}.{field}"),
                    depth + 1,
                    visiting,
                )?;
            }
        }
        SchemaKind::Array { items, .. } => {
            ensure_direct_output_safe(ir, items, path, depth + 1, visiting)?;
        }
        SchemaKind::OneOf { variants, .. } => {
            for variant in variants {
                ensure_direct_output_safe(ir, variant, path, depth + 1, visiting)?;
            }
        }
        SchemaKind::String(_)
        | SchemaKind::Integer { .. }
        | SchemaKind::Boolean
        | SchemaKind::Null => {}
    }
    Ok(())
}

fn ensure_transformed_output_safe(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    path: &str,
    depth: usize,
    visiting: &mut BTreeSet<String>,
) -> Result<(), String> {
    if depth > MAX_INLINE_SCHEMA_DEPTH {
        return Err(format!("transformed output exceeds depth limit at {path}"));
    }
    match kind {
        SchemaKind::Ref(name) => {
            if !visiting.insert(name.clone()) {
                return Err(format!(
                    "transformed output {path} contains recursive schema {name}"
                ));
            }
            let schema = ir
                .schemas
                .get(name)
                .ok_or_else(|| format!("unknown schema {name} at {path}"))?;
            ensure_transformed_output_safe(ir, &schema.kind, path, depth + 1, visiting)?;
            visiting.remove(name);
        }
        SchemaKind::JsonObject => {
            return Err(format!(
                "transformed output {path} cannot expose an arbitrary JSON object"
            ));
        }
        SchemaKind::Object(object) => {
            for (field, value) in &object.properties {
                if is_forbidden_projected_field(field) {
                    continue;
                }
                if is_transformed_output_sensitive_name(field) {
                    return Err(format!(
                        "transformed output {path}.{field} is sensitive but is not removed by the generated projector"
                    ));
                }
                ensure_transformed_output_safe(
                    ir,
                    value,
                    &format!("{path}.{field}"),
                    depth + 1,
                    visiting,
                )?;
            }
        }
        SchemaKind::Array { items, .. } => {
            ensure_transformed_output_safe(ir, items, path, depth + 1, visiting)?;
        }
        SchemaKind::OneOf { variants, .. } => {
            for variant in variants {
                ensure_transformed_output_safe(ir, variant, path, depth + 1, visiting)?;
            }
        }
        SchemaKind::String(_)
        | SchemaKind::Integer { .. }
        | SchemaKind::Boolean
        | SchemaKind::Null => {}
    }
    Ok(())
}

fn is_supervisor_owned_name(name: &str) -> bool {
    matches!(
        compact_name(name).as_str(),
        "requestid"
            | "idempotencykey"
            | "scope"
            | "clientscope"
            | "authorityscope"
            | "route"
            | "routingoverride"
            | "path"
            | "executiondomain"
            | "workspaceroot"
    )
}

fn is_transformed_output_sensitive_name(name: &str) -> bool {
    let value = compact_name(name);
    value.contains("credential")
        || value.contains("secret")
        || value.contains("password")
        || value.contains("privatekey")
        || value.contains("authorization")
        || value.contains("rawpath")
        || value == "path"
        || value == "uri"
        || value == "scope"
        || value.contains("routingoverride")
        || value.contains("executiondomain")
}

fn is_sensitive_name(name: &str) -> bool {
    let value = compact_name(name);
    value.contains("credential")
        || value.contains("secret")
        || value.contains("password")
        || value.contains("privatekey")
        || value.contains("authorization")
        || value.contains("header")
        || value.contains("rawpath")
        || value == "path"
        || value == "uri"
        || value == "scope"
        || value.contains("routingoverride")
        || value.contains("executiondomain")
}

fn is_forbidden_schema_name(name: &str) -> bool {
    let value = compact_name(name);
    value == "jsonvalue"
        || value.contains("credential")
        || value.contains("secret")
        || value.contains("diagnostic")
        || value.contains("privatepath")
        || value.contains("authorityscope")
        || value.contains("routingoverride")
}

fn compact_name(value: &str) -> String {
    value
        .chars()
        .filter(char::is_ascii_alphanumeric)
        .flat_map(char::to_lowercase)
        .collect()
}

fn object_schema<'a>(ir: &'a ProtocolIr, name: &str) -> Result<&'a ObjectSchema, String> {
    let schema = ir
        .schemas
        .get(name)
        .ok_or_else(|| format!("unknown object schema {name}"))?;
    match &schema.kind {
        SchemaKind::Object(object) => Ok(object),
        _ => Err(format!("schema {name} must be a closed object")),
    }
}

