fn renderer_ts_type(
    ir: &ProtocolIr,
    kind: &SchemaKind,
    renderer_input_variants: &BTreeMap<String, Vec<String>>,
    depth: usize,
    visiting: &mut BTreeSet<String>,
) -> Result<String, String> {
    if depth > MAX_INLINE_SCHEMA_DEPTH {
        return Err("Desktop renderer TypeScript type expansion exceeded depth limit".to_string());
    }
    Ok(match kind {
        SchemaKind::Ref(name) => {
            if !visiting.insert(name.clone()) {
                return Err(format!("recursive renderer TypeScript schema {name}"));
            }
            let schema = ir
                .schemas
                .get(name)
                .ok_or_else(|| format!("unknown renderer TypeScript schema {name}"))?;
            let value = if let Some(allowed) = renderer_input_variants.get(name) {
                let SchemaKind::OneOf { variants, .. } = &schema.kind else {
                    return Err(format!(
                        "renderer input variant target {name} is not a union"
                    ));
                };
                variants
                    .iter()
                    .filter(|variant| {
                        matches!(variant, SchemaKind::Ref(target) if allowed.contains(target))
                    })
                    .map(|variant| {
                        renderer_ts_type(
                            ir,
                            variant,
                            renderer_input_variants,
                            depth + 1,
                            visiting,
                        )
                    })
                    .collect::<Result<Vec<_>, _>>()?
                    .join(" | ")
            } else {
                renderer_ts_type(
                    ir,
                    &schema.kind,
                    renderer_input_variants,
                    depth + 1,
                    visiting,
                )?
            };
            visiting.remove(name);
            value
        }
        SchemaKind::String(StringSchema { enum_values, .. }) if !enum_values.is_empty() => {
            enum_values
                .iter()
                .map(|value| json_string(value))
                .collect::<Vec<_>>()
                .join(" | ")
        }
        SchemaKind::String(StringSchema {
            const_value: Some(value),
            ..
        }) => json_string(value),
        SchemaKind::String(_) => "string".to_string(),
        SchemaKind::Integer { .. } => "number".to_string(),
        SchemaKind::Boolean => "boolean".to_string(),
        SchemaKind::Null => "null".to_string(),
        SchemaKind::JsonObject => "Readonly<Record<string, unknown>>".to_string(),
        SchemaKind::Array { items, .. } => format!(
            "readonly ({})[]",
            renderer_ts_type(ir, items, renderer_input_variants, depth + 1, visiting,)?
        ),
        SchemaKind::Object(object) => {
            let mut value = String::from("{ ");
            for (field, field_kind) in &object.properties {
                let optional = if object.required.contains(field) {
                    ""
                } else {
                    "?"
                };
                value.push_str(&format!(
                    "readonly {}{}: {}; ",
                    ts_property(field),
                    optional,
                    renderer_ts_type(ir, field_kind, renderer_input_variants, depth + 1, visiting,)?
                ));
            }
            value.push('}');
            value
        }
        SchemaKind::OneOf { variants, .. } => variants
            .iter()
            .map(|variant| {
                renderer_ts_type(ir, variant, renderer_input_variants, depth + 1, visiting)
            })
            .collect::<Result<Vec<_>, _>>()?
            .join(" | "),
    })
}

